      'خاتم الأنبياء هو محمد ﷺ',
      'مسرى النبي ﷺ كان إلى المسجد الأقصى',
      'نزل الوحي لأول مرة في غار حراء',
      'صاحب النبي ﷺ في الهجرة هو أبو بكر الصديق',
      'أول من آمن من النساء هي خديجة بنت خويلد',
      'أول من آمن من الصبيان هو علي بن أبي طالب',
      'القرآن الكريم نزل في مدة 23 سنة',
      'السورة التي لا تبدأ بالبسملة هي سورة التوبة',
      'السورة التي تحتوي على بسملتين هي سورة النمل',
      'القبلة الأولى للمسلمين كانت المسجد الأقصى',
      'بُني المسجد النبوي في المدينة المنورة',
      'الصحابي الذي لقب بذي النورين هو عثمان بن عفان',
      'الصلاة عماد الدين ومن تركها فقد كفر',
      'الزكاة ركن من أركان الإسلام تجب في مال الأغنياء',
      'الحج واجب على كل مسلم مستطيع مرة في العمر',
      'الصيام جنة ووقاية من النار',
