    questions.addAll([
      'أعظم آية في القرآن الكريم هي آية الكرسي',
      'أطول سورة في القرآن هي سورة البقرة',
      'أقصر سورة في القرآن هي سورة الكوثر',
      'سيد الشهداء هو حمزة بن عبد المطلب',
      'أول مؤذن في الإسلام هو بلال بن رباح',
