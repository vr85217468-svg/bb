    // 13. الكتب السماوية (24 سؤال)
    for (var book in holyBooks) {
      questions.add('نزل ${book['book']} على ${book['prophet']}');
      questions.add(
        'ما هو الكتاب الذي نزل على ${book['prophet']}؟ الإجابة: ${book['book']}',
      );
      questions.add('${book['fact']} هو ${book['book']}');
      questions.add('على من نزل ${book['book']}؟ الإجابة: ${book['prophet']}');
    }

    // 14. أسماء الله الحسنى (60 سؤال)
    for (var name in allahNames) {
      questions.add('معنى اسم الله ${name['name']} هو ${name['meaning']}');
      questions.add(
        'ما معنى اسم الله ${name['name']}؟ الإجابة: ${name['meaning']}',
      );
      questions.add('${name['meaning']} يعني اسم الله ${name['name']}');
    }

    // 15. الملائكة ووظائفهم (40 سؤال)
    for (var angel in angels) {
      questions.add('الملك ${angel['name']} مهمته ${angel['role']}');
      questions.add(
        'ما مهمة الملك ${angel['name']}؟ الإجابة: ${angel['role']}',
