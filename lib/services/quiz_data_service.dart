    for (var p in prophetAges) {
      questions.add('عاش ${p['name']} ${p['age']}');
      questions.add('كم سنة عاش ${p['name']}؟ الإجابة: ${p['age']}');
      questions.add('${p['fact']} هو ${p['name']} الذي عاش ${p['age']}');
      questions.add('هل عاش ${p['name']} ${p['age']}؟ الإجابة: نعم');
