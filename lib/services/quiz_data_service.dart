    for (var h in famousHadith) {
      questions.add('حديث "${h['hadith']}" رواه ${h['narrator']}');
      questions.add('من راوي حديث "${h['hadith']}"؟ الإجابة: ${h['narrator']}');
      questions.add('موضوع حديث "${h['hadith']}" هو ${h['topic']}');
      questions.add(
        'هل حديث "${h['hadith']}" في موضوع ${h['topic']}؟ الإجابة: نعم',
      );
    }

    // 19. الخلفاء والحكام (50 سؤال)
    for (var c in caliphs) {
      questions.add('الخليفة ${c['name']} من ${c['era']}');
      questions.add('حكم ${c['name']} مدة ${c['period']}');
      questions.add('من إنجازات ${c['name']}: ${c['achievement']}');
      questions.add('كم مدة حكم ${c['name']}؟ الإجابة: ${c['period']}');
      questions.add(
        'هل ${c['achievement']} من إنجازات ${c['name']}؟ الإجابة: نعم',
      );
    }

    // 20. المصطلحات الإسلامية (45 سؤال)
    for (var t in islamicTerms) {
      questions.add('${t['term']} يعني ${t['meaning']}');
      questions.add('ما معنى ${t['term']}؟ الإجابة: ${t['meaning']}');
      questions.add('${t['meaning']} هو تعريف ${t['term']}');
    }

